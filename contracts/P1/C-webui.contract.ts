/** Debounce delay (ms) applied to both the live preview update and the
 *  WCAG contrast check triggered by token value edits. (REQ-024, REQ-026) */
export const DEBOUNCE_MS = 300;

// ---------------------------------------------------------------------------
// Color format
// ---------------------------------------------------------------------------

/**
 * The only accepted color formats for token value input (REQ-023).
 * Values that do not match one of these three patterns MUST be rejected
 * client-side before any save or preview action is triggered.
 *
 * Accepted patterns:
 *   #RGB       — three-digit hex shorthand, e.g. #f0a
 *   #RRGGBB    — six-digit hex, e.g. #ff0033
 *   rgb(R,G,B) — functional notation, integers 0-255, e.g. rgb(255, 0, 51)
 *
 * All other formats (hsl, oklch, named colors, etc.) are rejected.
 */
export type AcceptedColorFormat = string;

/** Runtime guard — test a raw string against this before saving or previewing. */
export const COLOR_FORMAT_RE =
  /^(#[0-9a-fA-F]{3}|#[0-9a-fA-F]{6}|rgb\(\s*\d{1,3}\s*,\s*\d{1,3}\s*,\s*\d{1,3}\s*\))$/;

// ---------------------------------------------------------------------------
// Inline minimal API-level types
// (mirror only the fields C-webui reads/writes from the C-mcp OpenAPI contract)
// ---------------------------------------------------------------------------

/** A project in the registry. */
export interface Project {
  id: string;
  name: string;
  parentId?: string;
  createdAt: string;
}

/** A single resolved token value (base merged with project overrides). */
export interface ResolvedToken {
  key: string;
  category: string;
  value: string;
  source: "base" | "override";
  isSemantic: boolean;
  semanticRef?: string;
  /**
   * Version integer forwarded as the If-Match header value on PUT (REQ-028).
   */
  version: string;
}

/** A single resolved component spec (base merged with project overrides). */
export interface ResolvedComponentSpec {
  id: string;
  name: string;
  variants: string[];
  states: string[];
  accessibilityNotes: string;
  usageRules: string;
  hasOverride: boolean;
}

/** Payload for PUT .../components/{componentId}/override. */
export interface ComponentOverrideInput {
  usageRules?: string;
  variants?: string[];
}

/** Response from POST /api/validate/color-pair. */
export interface ContrastResult {
  pairId: string;
  foreground: string;
  background: string;
  ratio: number;
  requiredRatio: number;
  passes: boolean;
}

/** An AI-proposed change (P3). */
export interface Proposal {
  id: string;
  projectId: string;
  targetType: "token" | "component";
  targetKey: string;
  proposedValue: string;
  rationale: string;
  status: "pending" | "approved" | "rejected";
  createdAt: string;
}

/** An audit record for a proposal decision (P3). */
export interface ProposalAuditRecord {
  proposalId: string;
  action: "approved" | "rejected";
  note?: string;
  decidedAt: string;
}

