// ---------------------------------------------------------------------------
// 5. WebUiApiClient — the C-mcp REST client interface
// ---------------------------------------------------------------------------

/**
 * The API client interface that C-webui must implement (or inject) to
 * communicate with C-mcp. All methods are async. C-webui MUST NOT call
 * C-tokens, C-components, or C-registry directly.
 *
 * Implementations should map HTTP errors to thrown Error instances with a
 * `.status` number property so callers can distinguish 409 CONFLICT from
 * other errors.
 */
export interface WebUiApiClient {
  // Projects
  listProjects(): Promise<Project[]>;
  createProject(name: string, parentId?: string): Promise<Project>;
  getProject(projectId: string): Promise<Project>;
  deleteProject(projectId: string): Promise<void>;

  // Tokens
  listTokens(projectId: string, category?: string): Promise<ResolvedToken[]>;

  /**
   * PUT /api/projects/{projectId}/tokens/{key}
   *
   * OCC (REQ-028): `ifMatch` must be the `version` from the last-fetched
   * ResolvedToken and is sent as the HTTP `If-Match` header.
   * On 409 CONFLICT, the implementation throws an error with `.status === 409`.
   * The UI then renders data-testid="save-error" with "conflict" text (REQ-028).
   */
  upsertTokenOverride(
    projectId: string,
    key: string,
    value: string,
    ifMatch: string,
  ): Promise<ResolvedToken>;

  deleteTokenOverride(projectId: string, key: string): Promise<ResolvedToken>;

  // Components
  listComponents(projectId: string): Promise<ResolvedComponentSpec[]>;
  getComponent(projectId: string, componentId: string): Promise<ResolvedComponentSpec>;
  upsertComponentOverride(
    projectId: string,
    componentId: string,
    input: ComponentOverrideInput,
  ): Promise<ResolvedComponentSpec>;
  deleteComponentOverride(projectId: string, componentId: string): Promise<ResolvedComponentSpec>;

  /**
   * POST /api/validate/color-pair
   *
   * ALWAYS an API call — never computed client-side (REQ-026).
   * Called on the DEBOUNCE_MS debounce, same window as the live preview.
   * Badge state persists after save; it is NOT cleared on successful PUT.
   */
  validateColorPair(
    pairId: string,
    foreground: AcceptedColorFormat,
    background: AcceptedColorFormat,
  ): Promise<ContrastResult>;

  // Proposals (P3)
  listProposals(projectId: string): Promise<Proposal[]>;
  listAuditRecords(projectId: string): Promise<ProposalAuditRecord[]>;
  approveProposal(proposalId: string): Promise<Proposal>;
  /**
   * @param note  Optional rejection rationale bound to
   *              data-testid="rejection-note-input" (REQ-033).
   */
  rejectProposal(proposalId: string, note?: string): Promise<Proposal>;
}
