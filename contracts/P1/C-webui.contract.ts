// ---------------------------------------------------------------------------
// 2. Token UI state
// ---------------------------------------------------------------------------

/**
 * Per-row UI state for the token editor (REQ-023, REQ-024, REQ-025, REQ-028).
 *
 * Rendered with:
 *   data-testid="token-row-{key}"
 *   data-source="base" | "override"
 *
 * `editValue` is present only while the row is in editing mode.
 * `isDirty` is true when editValue !== value.
 * `previewVisible` gates rendering of data-testid="token-preview".
 * `previewError` is set when the value is not a valid AcceptedColorFormat;
 *   triggers data-testid="preview-error" and disables the save button.
 * `saveError` is set on a 409 CONFLICT response from PUT (REQ-028);
 *   triggers data-testid="save-error" with "conflict" text.
 */
export interface TokenRowState {
  key: string;
  category: string;
  value: string;
  source: "base" | "override";
  isSemantic: boolean;
  semanticRef?: string;
  version: string;
  editValue?: string;
  isDirty: boolean;
  isEditing: boolean;
  previewVisible: boolean;
  previewError?: string;
  saveError?: string;
}

/**
 * Per-pair WCAG badge state (REQ-026).
 *
 * Rendered as:
 *   data-testid="wcag-badge-fail-{pairId}"  when passes === false
 *   data-testid="wcag-badge-pass-{pairId}"  when passes === true
 *
 * Populated from ContrastResult; persists after save.
 */
export interface WcagBadgeState {
  pairId: string;
  passes: boolean;
  ratio: number;
  requiredRatio: number;
}

